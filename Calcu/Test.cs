using System;
using System.Collections.Generic;
using System.Text;

namespace Calcu
{
    public class Calc
    {
        public static int Suma(int a, int b)
        {
            return a + b;
        }

        public static int Rest(int n1, int n2)
        {
            return n1 - n2;
        }
        public static int Mult(int n1, int n2)
        {
            return n1 * n2;
        }
        public static int Div(int n1, int n2)
        {
            return n1 / n2;
        }

    }
}
